//Linked List Structures
// ---------------------

// In C
#include<stdio.h>
Struct Node {
    int data;
    struct Node* next;
};

// In C++

// 1. Singly Linked List
// #include <iostream>
#include<bits/stdc++.h>
using namespace std;

Class Node {    
    int data;
    Node* next;     
};


//2. Doubly Linked List
#include<bits/stdc++.h>
using namespace std;

Class Node {
    int data;
    Node* next, prev;
};

// 3. Circular Linked List
#include<bits/stdc++.h>
using namespace std;

Class Node {
    public:
    int data;
    Node* next;
};














